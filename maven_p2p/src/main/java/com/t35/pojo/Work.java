package com.t35.pojo;

/**
 * Work entity. @author MyEclipse Persistence Tools
 */

public class Work implements java.io.Serializable {

	// Fields

	private Integer id;
	private Integer userId;
	private String company;
	private Integer companySize;
	private String companyPost;
	private String city;
	private Integer workHistory;
	private String incomeImg;

	// Constructors

	/** default constructor */
	public Work() {
	}

	/** full constructor */
	public Work(Integer userId, String company, Integer companySize,
			String companyPost, String city, Integer workHistory,
			String incomeImg) {
		this.userId = userId;
		this.company = company;
		this.companySize = companySize;
		this.companyPost = companyPost;
		this.city = city;
		this.workHistory = workHistory;
		this.incomeImg = incomeImg;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getUserId() {
		return this.userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getCompany() {
		return this.company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public Integer getCompanySize() {
		return this.companySize;
	}

	public void setCompanySize(Integer companySize) {
		this.companySize = companySize;
	}

	public String getCompanyPost() {
		return this.companyPost;
	}

	public void setCompanyPost(String companyPost) {
		this.companyPost = companyPost;
	}

	public String getCity() {
		return this.city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public Integer getWorkHistory() {
		return this.workHistory;
	}

	public void setWorkHistory(Integer workHistory) {
		this.workHistory = workHistory;
	}

	public String getIncomeImg() {
		return this.incomeImg;
	}

	public void setIncomeImg(String incomeImg) {
		this.incomeImg = incomeImg;
	}

}